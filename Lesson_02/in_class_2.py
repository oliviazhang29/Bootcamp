## functions
# no params no returns 
def print_hi():
    print("Hi")

# 1 input param no returns 
def print_backwards(string):
    print(string[::-1])

# 2 input params 1 return
def calculate_area(l, w):
    return l*w

# multiple inputs and multiple outputs
def min_max(nums):
    return min(nums), max(nums)